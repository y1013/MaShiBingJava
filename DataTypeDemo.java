/*
数据类型：
      Java是强类型的语言
	  整数类型：byte short int long(不同类型表示不同长度)
	      byte：使用一个字节存储 范围：-128-127
	      short:使用两个字节存储 范围：-3268-326767
	      int： 使用四个字节存储 范围：正负21亿
	      long：使用八个字节存储 范围：。。。。
		  默认整数类型 
	  浮点类型：float double
	      默认double类型
		  float后要添加f
		  浮点类型会损失精度
	  字符类型：char
	      占用两个字节，使用时用''表示
	  布尔类型：true false
*/


public class DataTypeDemo{
	
	public static void main(String[] args){
		 byte b=129;
		 //表示一个字符
		 char a='A';
		 //表示一个字符串，字符序列
		 String s="A";
	}
} 
